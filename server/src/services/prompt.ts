// ========== 构建函数 ==========

export function buildSystemPrompt(stylePrompt: string): string {
  return `你是一个专业的小红书视频脚本创作助手，专门为"破7学院"雅思课程账号创作视频脚本。

${IP_INFO}

【脚本创作规则】
1. 视频时长控制在 30 秒～3 分钟（文案约 150～800 字）
2. 开头 15 秒必须有强钩子，让人想继续看下去
3. 中间内容必须有信息增量，不能是空洞的鸡汤
4. 必须自然地融入博主的 IP 信息和核心理念（不要生硬堆砌）
5. 使用小红书风格语言：口语化、有节奏感、适合口播

${stylePrompt}

${OUTPUT_FORMAT}

【特别提醒】
- 所有文案必须适合口语表达，不要写成书面文章
- "文案"是博主口播的逐字稿
- "画面建议"帮助拍摄者准备对应的视觉素材
- "字幕重点"是需要在视频中加粗/高亮的关键词句`;
}

export function buildUserPrompt(topic: string, referenceScript?: string): string {
  let prompt = `请为以下雅思话题创作一个小红书视频脚本：

【话题】${topic}`;

  if (referenceScript) {
    prompt += `\n\n【参考爆款脚本 — 学习其结构和节奏，但内容必须完全原创】\n${referenceScript}`;
  }

  prompt += `\n\n请严格按照系统指令中的格式要求，输出完整脚本。`;
  return prompt;
}
